package com.example.data.javaset.list;

public class QueueSort {

    public int level;
    public String name;

    public QueueSort(int level, String name) {
        this.level = level;
        this.name = name;
    }

    public int level() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
